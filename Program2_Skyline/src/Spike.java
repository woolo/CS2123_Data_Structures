public class Spike{
	private int point;
	private int height;
	
	public Spike(int point, int height){
		this.point = point;
		this.height = height;
	}
	
	public int getPoint(){
		return point;
	}
	
	public int getHeight(){
		return height;
	}
	
	public void setPoint(int point){
		this.point = point;
	}
	
	public void setHeight(int height){
		this.height = height;
	}
}