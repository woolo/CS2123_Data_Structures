
public class Building {
	private int left;
	private int height;
	private int right;
	
	public Building(int left, int height, int right){
		this.left = left;
		this.height = height;
		this.right = right;
	}
	
	public int getLeft(){
		return left;
	}
	
	public int getHeight(){
		return height;
	}
	
	public int getRight(){
		return right;
	}
}
